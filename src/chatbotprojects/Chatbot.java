    private void btnTrainActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrainActionPerformed
        // TODO add your handling code here:
        String question = JOptionPane.showInputDialog(this, "Enter question :");
        String answer = JOptionPane.showInputDialog(this, "Enter answer :");
        
        if (question != null && answer != null && !question.isEmpty() && !answer.isEmpty()) {
            knowledge.put(question, answer);
            saveKnowledgeToFile("Source.txt");
            JOptionPane.showMessageDialog(this, "Thanks, I'll remember that!");
        } else {
            JOptionPane.showMessageDialog(this, "Please enter both question and answer!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTrainActionPerformed

