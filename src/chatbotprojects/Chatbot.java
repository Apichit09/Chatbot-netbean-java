    private void loadKnowledgeFromFile(String filename) {
        try (BufferedReader br = new BufferedReader(new FileReader(filename))) {
            String sCurrentLine;
            while ((sCurrentLine = br.readLine()) != null) {
                String[] information = sCurrentLine.split("_");
                if (information.length == 2) {
                    String k = information[0];
                    String v = information[1];
                    knowledge.put(k, v);
                }
            }
        } catch (IOException e) {
            // Handle file IO exception
            JOptionPane.showMessageDialog(this, "Error loading knowledge: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void saveKnowledgeToFile(String filename) {
        try (FileWriter writer = new FileWriter(filename)) {
            for (String key : knowledge.keySet()) {
                String value = knowledge.get(key);
                writer.write(key + "_" + value + "\n");
            }
        } catch (IOException e) {
            // Handle file IO exception
            JOptionPane.showMessageDialog(this, "Error saving knowledge: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void answer(String question) {
        String answer = knowledge.getOrDefault(question.toLowerCase(), "I don't understand.");
        jTextArea1.append("User: " + question + "\n");
        jTextArea1.append("ChatBot: " + answer + "\n\n");
    }
    

